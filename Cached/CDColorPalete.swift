import UIKit



extension UIColor {
    
    class func cdOrange() -> UIColor  {
        return UIColor(red:0.988, green:0.400 , blue:0.129, alpha: 1.0)
    }
    
    class func cdOffWhite() -> UIColor {
        return UIColor(red:0.965, green:0.965 , blue:0.937, alpha: 1.0)
    }
    
    class func cdWhite() -> UIColor {
        return UIColor(red:1.000, green:1.000 , blue:1.000, alpha: 1.0)
    }
}

